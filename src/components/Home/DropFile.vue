<template>
    <div class="dropfile" :class="{ active: active }" @mouseover="onMouseOver" @dragenter="onDragEnter"
        @dragover="onDragOver" @dragleave="onDragLeave" @drop="onDrop">
        <div class="layout">
            <div class="content" @click="picker.invoke()">
                <svg-icon icon-class="drop-here" class-name="drop-icon" />
                <!-- <DropHereIcon /> -->
                <p class="tip">Click or Drop Sketch Files Here</p>
            </div>
            <slot />
        </div>
    </div>
</template>
<script setup lang="ts">
import { ref, defineEmits, onUnmounted } from 'vue';
import { FilePicker } from '../common/filepicker';
import '@/assets/icons/svg/drop-here.svg';
const active = ref(false);

const emit = defineEmits<{
    (e: 'pick', value: File): void;
}>();

const picker = new FilePicker((file) => {
    emit('pick', file)
});

function onDrop(event: any) {
    if (isDropFile(event)) {
        const file = getDropFile(event);
        if (file) {
            emit('pick', file);
        }
    }
}

function isDropFile(event: DragEvent): boolean {
    const dropItems = Array.from(event.dataTransfer?.items || []);
    return dropItems.some((item) => item.kind === 'file')
}

function getDropFile(event: DragEvent): File | null {
    const dropItems = Array.from(event.dataTransfer?.items || []);
    for (let i = 0, len = dropItems.length; i < len; i++) {
        const item = dropItems[i];
        if (item.kind === 'file') {
            const file = item.getAsFile();
            if (file) return file;
        }
    }
    return null;
}

function onDragLeave() {
    active.value = false;
}

function onDragOver(event: DragEvent) {
    event.preventDefault();
    if (isDropFile(event)) {
        const file = getDropFile(event)
        active.value = true;
    }
}

function onDragEnter(event: DragEvent) {
    event.preventDefault();
    if (isDropFile(event)) {
        const file = getDropFile(event)
        active.value = true;
    }
}
function onMouseOver() {
    active.value = false;
}

onUnmounted(() => {
    picker.unmount();
})
</script>
